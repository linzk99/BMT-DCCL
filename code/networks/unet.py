class FFM(nn.Module):
    def __init__(self, class_num) -> None:
        super(FFM,self).__init__()

        params = {
                  'feature_chns': [16, 32, 64, 128, 256],
                  'dropout': [0.05, 0.1, 0.2, 0.3, 0.5],
                  'class_num': class_num,
                  'bilinear': False,
                  'acti_func': 'relu'}
        self.in_chns = params['feature_chns'][0] + 1
        self.class_num = params['class_num']
        
        self.convblock_1 = nn.Sequential(
            nn.Conv2d(in_channels=self.in_chns, out_channels=self.in_chns, kernel_size=3, padding=1),
            nn.BatchNorm2d(self.in_chns),
            nn.ReLU()
        )
        self.gap = nn.AdaptiveAvgPool2d(1)
        self.convblock_2 = nn.Sequential(
            nn.Conv2d(in_channels=self.in_chns, out_channels=self.in_chns, kernel_size=1, padding=0),
            nn.BatchNorm2d(self.in_chns),
            nn.ReLU()
        ) 
        self.convblock_3 = nn.Sequential(
            nn.Conv2d(in_channels=self.in_chns, out_channels=self.in_chns, kernel_size=1, padding=0),
            nn.BatchNorm2d(self.in_chns),
            nn.Sigmoid()
        ) 

        self.out_conv = nn.Conv2d(in_channels=self.in_chns, out_channels=self.class_num,
                                  kernel_size=3, padding=1)

    def forward(self,x):
        x1 = self.convblock_1(x)
        x2 = self.gap(x1)
        x2 = self.convblock_2(x2)
        x2 = self.convblock_3(x2)
        x2 = torch.mul(x1,x2)
        x2 = torch.add(x1,x2)

        self.x0 = x2 
        return self.out_conv(x2)
    def get_fea(self):
        return self.x0
    
