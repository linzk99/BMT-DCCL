class UNet_BMT_DCCL(nn.Module):

    def __init__(self, in_chns, class_num, 
                 deep_supervision=True, merging='attention'):
        super(UNet_BMT_DCCL, self).__init__()

        params = {'in_chns': in_chns,
                  'feature_chns': [16, 32, 64, 128, 256],
                  'dropout': [0.05, 0.1, 0.2, 0.3, 0.5],
                  'class_num': class_num,
                  'bilinear': False,
                  'acti_func': 'relu'}
        self.class_num = class_num
        self.encoder = Encoder(params)
        self.decoder = Decoder_multiout_FFM(params)
        self.FAM = FFM(class_num)
        
        
        self.predictors = nn.ModuleList([
            nn.Conv2d(in_channels=(1<<i)*16, out_channels=1, kernel_size=1)
            for i in reversed(range(5))
        ])
        
        self.merging = merging
        if merging == 'attention':
            self.queries = nn.ModuleList([
                nn.Conv2d(in_channels=(1<<i)*16, out_channels=1, kernel_size=1)
                for i in reversed(range(5))
            ])
        else:
            
            pass
        self.deep_supervision = deep_supervision

        self.out_conv = nn.Conv2d(1, 1, kernel_size=1)
        self.out_conv_1 = nn.Conv2d(1, 1, kernel_size=1)

        
        dim_in = 16
        feat_dim = 32
        self.projection_head = nn.Sequential(
            nn.Linear(dim_in, feat_dim),
            nn.BatchNorm1d(feat_dim),
            nn.ReLU(inplace=True),
            nn.Linear(feat_dim, feat_dim)
        )
        self.prediction_head = nn.Sequential(
            nn.Linear(feat_dim, feat_dim),
            nn.BatchNorm1d(feat_dim),
            nn.ReLU(inplace=True),
            nn.Linear(feat_dim, feat_dim)
        )
        for class_c in range(class_num):
            selector = nn.Sequential(
                nn.Linear(feat_dim, feat_dim),
                nn.BatchNorm1d(feat_dim),
                nn.LeakyReLU(negative_slope=0.2, inplace=True),
                nn.Linear(feat_dim, 1)
            )
            self.__setattr__('contrastive_class_selector_' + str(class_c), selector)

        for class_c in range(class_num):
            selector = nn.Sequential(
                nn.Linear(feat_dim, feat_dim),
                nn.BatchNorm1d(feat_dim),
                nn.LeakyReLU(negative_slope=0.2, inplace=True),
                nn.Linear(feat_dim, 1)
            )
            self.__setattr__('contrastive_class_selector_memory' + str(class_c), selector)
    
    def forward_projection_head(self, features):
        return self.projection_head(features)

    def forward_prediction_head(self, features):
        return self.prediction_head(features)
    
    def forward(self, x):
        B, _, H, W = x.shape

        feature = self.encoder(x)
        seg, multilevel_features = self.decoder(feature)
        
        
        predictions_list = []
        full_scale_preds = []
        for feature_map, predictor in zip(multilevel_features, self.predictors):
            prediction = predictor(feature_map)
            predictions_list.append(prediction)
            full_scale_preds.append(F.interpolate(prediction, size=(H, W), mode='bilinear', align_corners=True))

        predictions = torch.cat(full_scale_preds, dim=1)
        
        
        if self.merging == 'attention':
            queries = [F.interpolate(q(feat), size=(H, W), mode='bilinear', align_corners=True)
                    for q, feat in zip(self.queries, multilevel_features)]
            queries = torch.cat(queries, dim=1)
            queries = queries.reshape(B, -1, 1, H, W)
            attn = F.softmax(queries, dim=1)
            predictions = predictions.reshape(B, -1, 1, H, W)
            edge_combined_prediction = torch.sum(attn * predictions, dim=1)
           
            edge_combined_prediction_1 = self.out_conv_1(edge_combined_prediction)
            edge_combined_prediction = self.out_conv(edge_combined_prediction)
            
        else:
            edge_combined_prediction = predictions_list[-1]

        seg_edge = torch.cat((seg, edge_combined_prediction_1), dim=1)
        output = self.FAM(seg_edge)
        
        self.x0 = self.FAM.get_fea()
        
        if self.deep_supervision:
            return output, edge_combined_prediction, list(reversed(predictions_list)), seg
        else:
            return output, edge_combined_prediction
    







